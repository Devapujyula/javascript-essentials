/**
 *Comparison operators are used to compare two values and return either:

true (✅ condition is met)

false (❌ condition is not met)

These are core to logic, conditions, and if-statements in JavaScript.


| Operator | Name                  | Example     | Returns   |
| -------- | --------------------- | ----------- | --------- |
| `==`     | Equal to (loose)      | `5 == "5"`  | ✅ `true`  |
| `===`    | Strict equal          | `5 === "5"` | ❌ `false` |
| `!=`     | Not equal (loose)     | `5 != "5"`  | ❌ `false` |
| `!==`    | Strict not equal      | `5 !== "5"` | ✅ `true`  |
| `>`      | Greater than          | `7 > 5`     | ✅ `true`  |
| `<`      | Less than             | `3 < 5`     | ✅ `true`  |
| `>=`     | Greater than or equal | `6 >= 6`    | ✅ `true`  |
| `<=`     | Less than or equal    | `2 <= 3`    | ✅ `true`  |


✅ Used in:
if statements
loops
conditions in filtering, sorting
React conditional rendering (isLoggedIn === true ? ...)
 */

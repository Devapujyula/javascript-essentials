// slice()
var fruits = [
  "Apple",
  "Banana",
  "Cherries",
  "Dragon",
  "Muskmelon",
  "Watermelon",
  "Strawberry",
  "Peech",
];

console.log(fruits.slice(2, 4));
console.log(fruits.slice(1, 4));

